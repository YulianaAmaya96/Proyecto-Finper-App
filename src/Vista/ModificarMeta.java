    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nomb = this.nommbre.getText();
        long date = this.fecha1.getDate().getTime();
        long date2 = this.fecha2.getDate().getTime();
        float valorm = (float) this.valor.getValue();
        String descrip = this.descrpcion.getText();
        int prior = (int) this.prioridad.getValue();
        Controlador_Meta cm = new Controlador_Meta();
        meta = new Meta(nomb, descrip, new Date(date), new Date(date2), valorm, usuario, prior);
        if (this.estado.isSelected()) {
            meta.setEstado_objetivo("CUMPLIDA");
        }
        int rows = cm.Modificar_Meta(meta);
        if (rows != 0) {
            this.setVisible(false);
            ModificarMetas b = new ModificarMetas(this, true);
            b.setUsuario(usuario);
            b.setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

